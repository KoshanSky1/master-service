CREATE TABLE IF NOT EXISTS masters (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  number INTEGER NOT NULL,
  master_date TIMESTAMP WITHOUT TIME ZONE,
  amount INTEGER NOT NULL,
  description VARCHAR(255) NOT NULL,
  CONSTRAINT pk_master PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS details (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  master_id INTEGER NOT NULL,
  detail_name VARCHAR(255) NOT NULL,
  amount INTEGER NOT NULL,
  CONSTRAINT pk_detail PRIMARY KEY (id),
  CONSTRAINT details_masters_fk FOREIGN KEY (master_id) REFERENCES PUBLIC.masters(id)
            ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS message_logs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  created_date_time TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  message varchar(255) NOT NULL,
  CONSTRAINT pk_message_logs PRIMARY KEY (id)
);